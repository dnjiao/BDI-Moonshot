package hibernate;

public class FileQueue {
	private int id;
	private String status;
	
	
	public FileQueue() {
	}
	

	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}

	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}